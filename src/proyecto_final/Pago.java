    private void cargarCosto(){

    try{

        con = conexion.conectar();

        String sql =

        "SELECT costo_estimado " +
        "FROM pago p " +
        "JOIN solicitud_viaje s " +
        "ON p.id_solicitud=s.id_solicitud " +
        "WHERE s.matricula_pasajero=? " +
        "ORDER BY p.id_pago DESC";

        PreparedStatement ps =
        con.prepareStatement(sql);

        ps.setInt(
        1,
        matriculaPasajero
        );

        ResultSet rs =
        ps.executeQuery();

        if(rs.next()){

            double costo =
            rs.getDouble(
            "costo_estimado"
            );

            lblCosto.setText(
            "Costo total: $" +
            String.format("%.2f", costo)
            );

        }

        rs.close();
        ps.close();
        con.close();

    }

    catch(SQLException e){

        e.printStackTrace();

    }

}

