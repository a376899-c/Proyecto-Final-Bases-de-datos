    private boolean tieneTarjeta() {

    try {
        con = conexion.conectar();

        String sql =
        "SELECT * FROM tarjeta WHERE matricula_pasajero=?";

        PreparedStatement ps = con.prepareStatement(sql);
        ps.setInt(1, matriculaPasajero);

        ResultSet rs = ps.executeQuery();

        return rs.next();

    } catch (SQLException e) {
        e.printStackTrace();
    }

    return false;
}
    
    private void cargarTarjeta() {

    try {
        con = conexion.conectar();

        String sql =
        "SELECT numero_tarjeta, titular, vencimiento " +
        "FROM tarjeta WHERE matricula_pasajero=?";

        PreparedStatement ps = con.prepareStatement(sql);
        ps.setInt(1, matriculaPasajero);

        ResultSet rs = ps.executeQuery();

        if (rs.next()) {

            txtNumero.setText(rs.getString("numero_tarjeta"));
            txtTitular.setText(rs.getString("titular"));
            txtVencimiento.setText(rs.getString("vencimiento"));

        }

    } catch (SQLException e) {
        e.printStackTrace();
    }
}
    
