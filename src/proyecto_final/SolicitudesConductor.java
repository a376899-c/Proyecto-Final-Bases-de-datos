tablaSolicitudes.getSelectedRow();

if(fila==-1){

    System.out.println(
    "Selecciona una solicitud"
    );

    return;
}

int idSolicitud=

(Integer)
tablaSolicitudes.getValueAt(
fila,
0
);

try{

    con=conexion.conectar();

    String sql=

    "UPDATE solicitud_viaje " +
    "SET estado='Aceptada' " +
    "WHERE id_solicitud=?";

    PreparedStatement ps=
    con.prepareStatement(sql);

    ps.setInt(1,idSolicitud);

    ps.executeUpdate();

    String pasajero =
tablaSolicitudes.getValueAt(fila,1).toString();

String puntoRecogida =
tablaSolicitudes.getValueAt(fila,2).toString();

JOptionPane.showMessageDialog(
null,

"Has aceptado la solicitud de: " +
pasajero +

"\n\nPunto de recogida:\n" +
puntoRecogida +

"\n\nDirígete al punto para " +
"comenzar el viaje."
);
ViajeEnCursoConductor form =
new ViajeEnCursoConductor(
matriculaConductor
);

form.setVisible(true);

this.dispose();
cargarSolicitudes();

    cargarSolicitudes();

}
catch(SQLException e){

    e.printStackTrace();

}


