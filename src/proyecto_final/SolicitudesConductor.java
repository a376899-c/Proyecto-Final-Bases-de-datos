    private void cargarSolicitudes(){

    try{

        con = conexion.conectar();

        DefaultTableModel model =
        (DefaultTableModel) tablaSolicitudes.getModel();

        model.setRowCount(0);

        String sql =
        "SELECT s.id_solicitud, " +
        "p.nombre, " +
        "s.punto_recogida, " +
        "v.origen, " +
        "v.destino " +
        "FROM solicitud_viaje s " +
        "JOIN pasajero p " +
        "ON s.matricula_pasajero=p.matricula " +
        "JOIN viaje v " +
        "ON s.id_viaje=v.id_viaje " +
        "WHERE v.matricula_conductor=?";

        PreparedStatement ps =
        con.prepareStatement(sql);

        ps.setString(1, matriculaConductor);

        ResultSet rs =
        ps.executeQuery();

        while(rs.next()){

            model.addRow(new Object[]{

            rs.getInt("id_solicitud"),
            rs.getString("nombre"),
            rs.getString("punto_recogida"),
            rs.getString("origen"),
            rs.getString("destino")

        });

        }

        rs.close();
        ps.close();
        con.close();

    }

    catch(SQLException e){

        e.printStackTrace();

    }

}
